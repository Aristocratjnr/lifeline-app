import { Ionicons } from "@expo/vector-icons";
import { useFonts } from "expo-font";
import { useRouter } from "expo-router";
import React, { useState } from "react";
import {
  Dimensions,
  Image,
  ImageBackground,
  SafeAreaView,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
} from "react-native";

const { width, height } = Dimensions.get("window");

const SunBurnDetail: React.FC = () => {
  const router = useRouter();

  // Load fonts
  const [fontsLoaded] = useFonts({
    "JetBrainsMono-Regular": require("@/assets/fonts/JetBrainsMono-Regular.ttf"),
    "JetBrainsMono-Bold": require("@/assets/fonts/JetBrainsMono-Bold.ttf"),
  });

  const handleGoBack = () => {
    if (router.canGoBack()) {
      router.back();
    } else {
      router.replace('/dashboard');
    }
  };

  if (!fontsLoaded) {
    return <View style={styles.container} />;
  }

  return (
    <ImageBackground 
      source={require('../../../assets/images/blur.png')} 
      style={styles.backgroundImage}
      resizeMode="cover"
    >
      <View style={styles.overlay} />
      <SafeAreaView style={styles.container}>
        {/* Header */}
        <View style={styles.header}>
          <TouchableOpacity 
            style={styles.backButton}
            onPress={handleGoBack}
          >
            <Ionicons name="arrow-back" size={24} color="#000" />
          </TouchableOpacity>
          <Text style={styles.headerTitle}>SUN BURN</Text>
        </View>
        
        <ScrollView style={styles.content}>
          <View style={styles.illustrationContainer}>
            <Image 
              source={require('../../../assets/images/sun_burn.png')} 
              style={styles.illustration}
              resizeMode="contain"
            />
          </View>
          
          <View style={styles.infoContainer}>
            <Text style={styles.sectionTitle}>What to do:</Text>
            <Text style={styles.description}>
              1. Get out of the sun and into a cool, shaded area.{'\n'}
              2. Take a cool (not cold) bath or shower to soothe the skin.{'\n'}
              3. Apply cool, damp cloths or compresses to the affected areas.{'\n'}
              4. Drink extra water to prevent dehydration and help your body heal.{'\n'}
              5. Apply a moisturizer containing aloe vera or soy to help soothe the skin.{'\n'}
              6. Take over-the-counter pain relievers like ibuprofen or aspirin to reduce swelling and discomfort.{'\n'}
              7. Do NOT pop blisters if they form, as this can lead to infection.
            </Text>
            
            <Text style={styles.sectionTitle}>When to seek medical help:</Text>
            <Text style={styles.description}>
              • Severe sunburn with blistering over a large area of the body{'\n'}
              • Fever above 100°F (37.8°C){'\n'}
              • Severe pain that doesn't improve with over-the-counter medications{'\n'}
              • Signs of infection in blisters (increasing redness, warmth, pus, red streaking){'\n'}
              • Headache, nausea, chills, or dizziness{'\n'}
              • Confusion or fainting{'\n'}
              • Severe dehydration (dizziness when standing, little or no urination){'\n'}
              • Sunburn on an infant under 1 year old
            </Text>
          </View>
        </ScrollView>
      </SafeAreaView>
    </ImageBackground>
  );
};

