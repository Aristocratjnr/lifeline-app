import { Ionicons } from "@expo/vector-icons";
import { useFonts } from "expo-font";
import { useRouter } from "expo-router";
import React, { useState } from "react";
import {
  Dimensions,
  Image,
  ImageBackground,
  SafeAreaView,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
} from "react-native";

const { width, height } = Dimensions.get("window");

const SplinterDetail: React.FC = () => {
  const router = useRouter();

  // Load fonts
  const [fontsLoaded] = useFonts({
    "JetBrainsMono-Regular": require("@/assets/fonts/JetBrainsMono-Regular.ttf"),
    "JetBrainsMono-Bold": require("@/assets/fonts/JetBrainsMono-Bold.ttf"),
  });

  const handleGoBack = () => {
    if (router.canGoBack()) {
      router.back();
    } else {
      router.replace('/dashboard');
    }
  };

  if (!fontsLoaded) {
    return <View style={styles.container} />;
  }

  return (
    <ImageBackground 
      source={require('../../../assets/images/blur.png')} 
      style={styles.backgroundImage}
      resizeMode="cover"
    >
      <View style={styles.overlay} />
      <SafeAreaView style={styles.container}>
        {/* Header */}
        <View style={styles.header}>
          <TouchableOpacity 
            style={styles.backButton}
            onPress={handleGoBack}
          >
            <Ionicons name="arrow-back" size={24} color="#000" />
          </TouchableOpacity>
          <Text style={styles.headerTitle}>SPLINTER</Text>
        </View>
        
        <ScrollView style={styles.content}>
          <View style={styles.illustrationContainer}>
            <Image 
              source={require('../../../assets/images/splinter.png')} 
              style={styles.illustration}
              resizeMode="contain"
            />
          </View>
          
          <View style={styles.infoContainer}>
            <Text style={styles.sectionTitle}>What to do:</Text>
            <Text style={styles.description}>
              1. Wash your hands thoroughly with soap and water.{'\n'}
              2. Clean the area around the splinter with soap and water.{'\n'}
              3. Sterilize a needle or tweezers with rubbing alcohol.{'\n'}
              4. If the splinter is sticking out, gently pull it out with tweezers in the direction it entered.{'\n'}
              5. If the splinter is under the skin, use a sterilized needle to gently lift the skin above the splinter, then pull it out with tweezers.{'\n'}
              6. Wash the area again with soap and water.{'\n'}
              7. Apply an antibiotic ointment and cover with a bandage.
            </Text>
            
            <Text style={styles.sectionTitle}>When to seek medical help:</Text>
            <Text style={styles.description}>
              • The splinter is deep under the skin and you can't reach it{'\n'}
              • The splinter is under a fingernail or toenail{'\n'}
              • The splinter is made of glass, metal, or another material that may break{'\n'}
              • The area becomes red, swollen, warm, or pus-filled (signs of infection){'\n'}
              • You develop red streaking from the wound{'\n'}
              • You develop a fever{'\n'}
              • The splinter is in your eye or near sensitive areas
            </Text>
          </View>
        </ScrollView>
      </SafeAreaView>
    </ImageBackground>
  );
};

