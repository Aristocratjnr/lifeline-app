import { Ionicons } from "@expo/vector-icons";
import { useFonts } from "expo-font";
import { useRouter } from "expo-router";
import React, { useState } from "react";
import {
  Dimensions,
  Image,
  ImageBackground,
  SafeAreaView,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
} from "react-native";

const { width, height } = Dimensions.get("window");

const HivesDetail: React.FC = () => {
  const router = useRouter();

  // Load fonts
  const [fontsLoaded] = useFonts({
    "JetBrainsMono-Regular": require("@/assets/fonts/JetBrainsMono-Regular.ttf"),
    "JetBrainsMono-Bold": require("@/assets/fonts/JetBrainsMono-Bold.ttf"),
  });

  const handleGoBack = () => {
    if (router.canGoBack()) {
      router.back();
    } else {
      router.replace('/dashboard');
    }
  };

  if (!fontsLoaded) {
    return <View style={styles.container} />;
  }

  return (
    <ImageBackground 
      source={require('../../../assets/images/blur.png')} 
      style={styles.backgroundImage}
      resizeMode="cover"
    >
      <View style={styles.overlay} />
      <SafeAreaView style={styles.container}>
        {/* Header */}
        <View style={styles.header}>
          <TouchableOpacity 
            style={styles.backButton}
            onPress={handleGoBack}
          >
            <Ionicons name="arrow-back" size={24} color="#000" />
          </TouchableOpacity>
          <Text style={styles.headerTitle}>HIVES</Text>
        </View>
        
        <ScrollView style={styles.content}>
          <View style={styles.illustrationContainer}>
            <Image 
              source={require('../../../assets/images/hives.png')} 
              style={styles.illustration}
              resizeMode="contain"
            />
          </View>
          
          <View style={styles.infoContainer}>
            <Text style={styles.sectionTitle}>What to do:</Text>
            <Text style={styles.description}>
              1. Avoid scratching the hives to prevent infection and worsening of symptoms.{'\n'}
              2. Apply a cool, wet compress or take a cool bath to soothe itching.{'\n'}
              3. Wear loose, lightweight clothing to avoid further irritation.{'\n'}
              4. Take an over-the-counter antihistamine like diphenhydramine or loratadine.{'\n'}
              5. Apply calamine lotion or hydrocortisone cream to particularly itchy areas.{'\n'}
              6. Identify and avoid potential triggers (foods, medications, stress, heat, etc.).{'\n'}
              7. Keep the room cool and use fans to prevent overheating.
            </Text>
            
            <Text style={styles.sectionTitle}>When to seek medical help:</Text>
            <Text style={styles.description}>
              • Difficulty breathing or swallowing{'\n'}
              • Swelling of the lips, tongue, or throat{'\n'}
              • Dizziness or fainting{'\n'}
              • Rapid pulse or feeling of impending doom{'\n'}
              • Hives that persist for more than 6 weeks (chronic urticaria){'\n'}
              • Signs of infection in scratched areas{'\n'}
              • Hives accompanied by fever or joint pain{'\n'}
              • Severe abdominal pain or vomiting
            </Text>
          </View>
        </ScrollView>
      </SafeAreaView>
    </ImageBackground>
  );
};

